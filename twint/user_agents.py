def get_random_user_agent(wa=None) -> str:
    # noinspection PyBroadException
    try:
        if wa:
            return default_user_agent
        return UserAgent(verify_ssl=False, use_cache_server=False).random
    except Exception:
        return random.choice(user_agent_list)
